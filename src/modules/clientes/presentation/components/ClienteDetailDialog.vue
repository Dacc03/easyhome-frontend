            <div class="detail-item">
              <span class="detail-label">Última Actualización:</span>
              <span class="detail-value">{{ formatDate(cliente.updatedAt) }}</span>
            </div>
          </div>
        </div>
      </div>
    </div>

    <template #footer>
      <div class="flex justify-content-end">
        <Button
            label="Cerrar"
